#include <tgmath.h>
#include <iostream>

using namespace std;

/*
 * Задача 1
 * Вычислить среднее арифметическое значение трёх 
 * введённых чисел
 */

int main(){
    int a = 2;

    cout << ++a << endl;

    cout << a++ << endl;
    cout << a << endl;
    int y = a << 2;

    cout << y << endl;
    return 0;
}
